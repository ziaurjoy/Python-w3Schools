
# Many Values to Multiple Variables
x, y, z = "Bangladesh", "India", "Pakistan"

print(x)
print(y)
print(z)

# One Value of Multiple Variable
x = y = z = "Hello Bangladesh"

print(x)
print(y)
print(z)

# Unpack a Collection
city = ["Dhaka", "Feni", "Bogura"]
x, y, z = city
print(x)
print(y)
print(z)

a, b, c = ["Dhaka", "Feni", "Bogura"]
print(a)
print(b)
print(c)

