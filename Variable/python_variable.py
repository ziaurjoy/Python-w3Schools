# Casting

x = str(5) # "5"
y = float(5) # 5.0
z = int(5) # 5

print(x)
print(y)
print(z)


# Get the Type

print(type(x))
print(type(y))
print(type(z))

# Single or Double Quotes?

x = 'hello'
