
# uper and lower
a = "Hello, World"
print(a.upper())
print(a.lower())

# strip white space remove
b = "     Hello, Bangladesh  "
print(b.strip())
print(b.lstrip())
print(b.rstrip())

# replace string

a = "Hello, Bangladesh"
print(a.replace("Bangladesh", "Dhaka"))

# split separator function
a = "Hello, Bangladesh"
print(a.split(','))


