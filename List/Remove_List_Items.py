
# remove()
thislist = ["apple", "banana", "cherry"]
thislist.remove("banana")
print(thislist)

#pop()
thislist = ["apple", "banana", "cherry"]
thislist.pop()
print(thislist)

# pop() index
thislist = ["apple", "banana", "cherry"]
thislist.pop(1)
print(thislist)

# delete index del
thislist = ["apple", "banana", "cherry"]
del thislist[0]
print(thislist)

thislist = ["apple", "banana", "cherry"]
# del thislist
print(thislist)

thislist.clear()
print(thislist)

